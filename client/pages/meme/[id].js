import { useRouter } from "next/router";
import { useEffect, useState } from "react";
import axios from "axios";
import Link from "next/link";

const Meme = ({ user, meme }) => {
  const router = useRouter();
  const [title, setTitle] = useState(meme.title);
  const [image, setImage] = useState(meme.image);
  const [like, setLike] = useState(false);
  const [likeCount, setLikeCount] = useState(0);
  const [tag, setTag] = useState([]);
  const [author, setAuthor] = useState({ username: "", displayname: "" });
  const [loading, setLoading] = useState(false);
  const { id } = router.query;

  const handleLike = async () => {
    if (user) {
      await axios.post("http://localhost:5000/api/likeMeme", {
        userId: user._id,
        memeId: id,
      });
      setLike(!like);
      if (like) {
        setLikeCount(likeCount - 1);
      } else {
        setLikeCount(likeCount + 1);
      }
    } else {
      router.push("/login");
    }
  };

  const handleShare = () => {
    navigator.clipboard.writeText(`${window.location.host}/meme/${id}`);
    swal("Link Copied", "Paste the link and share the meme with others!");
  };

  const loadData = async () => {
    const authorId = null;
    await axios
      .get(`${process.env.SERVER_URL}/api/meme/${id}`)
      .then((res) => {
        if (user) {
          setLike(res.data.likedBy.includes(user._id));
        }
        setLikeCount(res.data.likedBy.length);
        setImage(res.data.image);
        setTitle(res.data.title);
        setTag(res.data.tag);
        authorId = res.data.createdBy;
      })
      .catch((err) => {
        router.push("/");
        console.log(err.response);
      });
    await axios
      .get(`${process.env.SERVER_URL}/api/user/${authorId}`)
      .then((res) => {
        setAuthor(res.data);
      })
      .catch((err) => {
        console.log(err.response);
      });
  };

  useEffect(async () => {
    if (id) {
      setLoading(true);
      await loadData();
      setLoading(false);
    }
  }, [id]);

  return (
    <>
      {loading ? (
        <div className="text-center">
          <div className="spinner-grow" role="status">
            <span className="sr-only"></span>
          </div>
        </div>
      ) : (
        <>
          <div className="row mb-2">
            <div className="col-lg-9 col-sm-12">
              <div className="row">
                <div className="col-12">
                  <h4>{title}</h4>
                  <div className="meme bg-white">
                    <div className="text-center p-3">
                      <img
                        style={{
                          width: "auto",
                          height: "auto",
                          maxWidth: "500px",
                          maxHeight: "500px",
                        }}
                        className="rounded"
                        src={image.link}
                      />
                    </div>
                    <div className="mt-2 btn-group w-100">
                      <button
                        className="btn btn-left btn-light"
                        onClick={handleLike}
                      >
                        <i
                          className={
                            !user
                              ? "bi bi-hand-thumbs-up"
                              : like
                              ? "bi bi-hand-thumbs-up-fill"
                              : "bi bi-hand-thumbs-up"
                          }
                        ></i>
                        &nbsp;
                        {likeCount}
                      </button>
                      <button
                        className="btn btn-right btn-light"
                        onClick={handleShare}
                      >
                        <i className="bi bi-share"></i>
                      </button>
                    </div>
                  </div>
                </div>
                <div className="col-12">
                  <div className="meme bg-white p-3 mt-3">
                    <h5>Tag</h5>
                    <hr className="mt-2 mb-3" />
                    <div>
                      {tag.map((t, index) => {
                        return (
                          <div
                            key={index}
                            className="alert alert-secondary py-1 my-0 me-1 text-center"
                            style={{ width: "10%", display: "inline" }}
                          >
                            {t.label}
                          </div>
                        );
                      })}
                    </div>
                  </div>
                </div>
              </div>
            </div>
            <div className="col-lg-3 col-sm-12">
              <h4>Posted By</h4>
              <div className="meme bg-white p-3">
                <div className="profile-btn text-center mx-auto mb-2">
                  <p className="text-white">
                    {author.username.charAt(0).toUpperCase()}
                  </p>
                </div>
                <h5 className="text-center">{author.displayname}</h5>
                <div className="text-center">
                  <Link href={`/user/${author._id}`}>
                    <a className="nav-link pb-0">View Profile</a>
                  </Link>
                </div>
              </div>
            </div>
          </div>
        </>
      )}
    </>
  );
};

export async function getServerSideProps(context) {
  const { id } = context.query;
  let meme = { title: "", image: { link: "" } };
  await axios
    .get(`${process.env.SERVER_URL}/api/meme/${id}`)
    .then((res) => {
      meme = res.data;
    })
    .catch((err) => {});
  return {
    props: {
      meme: meme,
    },
  };
}

export default Meme;
